function getHeroBuildGroup(rawcode) {
    const data = HERO_BUILD_DATA[rawcode];
    return data ? data.group : null;
}

// Получить всех героев в указанной группе сборки
function getHeroesInGroup(group) {
    return Object.entries(HERO_BUILD_DATA)
        .filter(([_, data]) => data.group === group)
        .map(([rawcode, data]) => ({ rawcode, ...data }));
}
